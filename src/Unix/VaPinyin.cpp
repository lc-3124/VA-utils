    bool
VaUtils::Chinese::Char2Pinyin ( const std::string resource,
        std::string &     Pinyin )
{
    std::string ch;
    VaTui::Utf::getUtf8CharaInString ( resource, ch, 0 );

    auto it = Ch2PyMap.find ( ch );
    if ( it != Ch2PyMap.end () )
    {
        Pinyin = it->second;
    }
    else
    {
        // 未找到
        std::string save;
        VaTui::Utf::getUtf8CharaInString ( resource, save, 0 );
        Pinyin = save;
        return false;
    }
    return true;
}

